# MANTENIBILIDAD O MAINTENIBILITY

# Tiene que ser un software solido facil de mantener y modificar en el tiempo con una estructura clara y seguir buenas practicas
# Facil de reutilizar codigo, todos los componentes tienen que ser reutilizables para ahorrar tiempo y lineas de codigo
# Facil de entender por otro desarrollador siguiendo las buenas practicas para escribir variables, etc
# Tiene que ser extensible, debe permitir la posibilidad de extenderse sin afectar el codigo que ya existe

# Los 5 pricipios son:

# SRP
#       Single Resposability Principle o Principio de responsabilidad unica

#       Cada clase no tiene que hacer mas de una cosa, si en algun momento vemos que esa clase tiene que hacer mas de una cosa
#       tenemos que separar en 2 la clase y que cada clase se encargue solo de una cosa

#       Al hacer esto el codigo se vuelve mas legible, mas facil de mantener.

# OCP
#       Open/Closed Principle o Principio de abierto y cerrrado

#       Este principio nos dice que las entidades de software tienen que estar abiertas para la extencion pero cerradas para la modificacion
#       Basicamente lo que nos dice es que tendriamos que no tener problemas para agregarle funcionalidades a una clase

