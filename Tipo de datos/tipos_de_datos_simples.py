# Estos son los tipos de datos simples de Python.

# STRING O TEXTO

# Es un tipo de dato simple
# Los tipos de datos se pueden escribir de la siguiente manera

'esto es un dato string'  # Con comillas simples
"esto es un dato string"  # Con comillas dobles

"""esto es un dato string"""  # Con comillas dobles, triples
'''esto es un dato string'''  # Con comillas simples, triples

# La diferencia entre las comillas simples y las triples es que las comillas triples nos permiten escribir multiples lineas.

"""Esto es 
un string"""

# NUMEROS

14  # Numeros enteros o int.
14.4  # Numeros flotantes o float.

# DATOS BOOLEANOS

False  # Falso
True  # Verdadero
