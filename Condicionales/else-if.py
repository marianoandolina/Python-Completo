''' Condicionales else, if'''

ingreso_mensual = 5000

if ingreso_mensual > 10000:
    print('Estas bien en cualquier parte del mundo')

elif ingreso_mensual > 1000:
    print('Estas bien en latinoamerica')

else:
    print('Sos pobre')

