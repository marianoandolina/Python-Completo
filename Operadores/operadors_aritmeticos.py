

'''
Son operadores matematicos 
'''

# Suma y resta (+ y -)

suma = 10 + 5
resta = 12 - 5

# Multiplicacion y division (* y /)

mulltiplicacion = 2 * 2
division = 10 / 2

# La division nos devuelve siempre un dato float por mas que el resultado de un numero entero.

print(f'La suma es {suma}\nLa resta es {resta}\nLa multiplicacin es {mulltiplicacion}\nLa division es {division}')

# Potenciacion (**)

exponente = 2 ** 2
print(exponente)

# Division baja (//)

division_baja = 12 // 5  # Devuelve el resultado redondeado hacia abajo
print(division_baja)

# Resto o modulo (%)

resto = 12 % 7
print(resto)
